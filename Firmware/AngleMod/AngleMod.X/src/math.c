int8_t sin_lookup(uint16_t a)
{
    while (a > 360)
        a -= 360;
    
    if (a >= 270)
        return -table[360 - a];
    if (a >= 180)
        return -table[a - 180];
    if (a >= 90)
        return table[180 - a];
    return table[a];
}

